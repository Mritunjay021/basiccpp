/*
    *Struct
        - Structure is defined with struct keyword
        - Size of a structure is sum of all individual elements inside it
        - Struct is a collection of user defined data types stored at different memory location
        - Not memory effecient

    *Union
        - Union is defined with union keyword
        - Size of union is the largest element insidde it
        - Union is a collection of user defined data types stored at same memory location
        - Memory effecient

    *Enums
        - Enumearation is defined with enum keyword
        - Size of enum is 4 X (number of constant inside) as each constant is an integer of 4 byte
        - Enums is a collection of user defined data types for representing integer based contants



*/